#!/usr/bin/env node
// Generate .claude/AGENT_PRIMER.md — single file that sub-agents load instead of grepping neighbours.
// Runs on `npm run build` (which runs on dev/start). Zero runtime deps.

import { createRequire } from 'module';
import { execSync } from 'child_process';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';
import { readFileSync, writeFileSync } from 'fs';

const require = createRequire(import.meta.url);
const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const { buildSystemMap } = require(join(ROOT, 'server', 'services', 'system-map-service.js'));

const PRIMER_PATH = join(ROOT, '.claude', 'AGENT_PRIMER.md');

function readSafe(path) {
  try { return readFileSync(path, 'utf8'); } catch { return ''; }
}

function gitSha() {
  try { return execSync('git rev-parse --short HEAD', { cwd: ROOT }).toString().trim(); }
  catch { return 'unknown'; }
}

function extractClaudeMdSections(src) {
  // Keep: Stack, Architecture, File Map, Critical Patterns, Agent Routing, Rules.
  // Drop: How to Run, Env Vars (boilerplate), Workflows (too verbose for primer).
  const keepHeadings = [
    '## Stack',
    '## Architecture',
    '## File Map',
    '## Critical Patterns',
    '## Agent Routing',
    '## Rules',
  ];
  const lines = src.split('\n');
  const out = [];
  let keeping = false;
  for (const line of lines) {
    if (line.startsWith('## ')) {
      keeping = keepHeadings.some((h) => line.startsWith(h));
    }
    if (keeping) out.push(line);
  }
  return out.join('\n').trim();
}

function extractRecentHandoffs(src, n = 3) {
  const headerRe = /^##\s+\d{4}-\d{2}-\d{2}.*$/gm;
  const matches = [...src.matchAll(headerRe)];
  if (!matches.length) return '';
  const picks = matches.slice(-n);
  const first = picks[0].index;
  return src.slice(first).trim();
}

function renderRoutes(routes) {
  if (!routes.length) return '_none detected_';
  return routes.map((file) => {
    const lines = [`### \`${file.file}\``];
    for (const ep of file.endpoints) {
      const desc = ep.description ? ` — ${ep.description}` : '';
      lines.push(`- **${ep.method}** \`${ep.path}\`${desc}`);
    }
    return lines.join('\n');
  }).join('\n\n');
}

function renderModules(modules) {
  if (!modules.length) return '_none detected_';
  return modules.map((m) => {
    const view = m.referencedByView ? ` → view \`${m.referencedByView}\`` : '';
    const exp = m.exports.length ? ` exports: ${m.exports.map((e) => `\`${e}\``).join(', ')}` : '';
    return `- **${m.name}**${view}${exp ? ` —${exp}` : ''}`;
  }).join('\n');
}

function renderNotionDbs(dbs) {
  if (!dbs.length) return '_none detected_';
  return dbs.map((d) => {
    const props = d.propertiesSummary.length ? ` · props: ${d.propertiesSummary.slice(0, 6).join(', ')}${d.propertiesSummary.length > 6 ? '…' : ''}` : '';
    const purpose = d.purpose ? ` — ${d.purpose}` : '';
    return `- **${d.name}** \`${d.id.slice(0, 8)}…\`${purpose}${props}`;
  }).join('\n');
}

function renderSheets(sheets) {
  if (!sheets.length) return '_none detected_';
  return sheets.map((s) => {
    const status = s.idPresent ? '✓' : '✗';
    return `- ${status} \`${s.key}\` — ${s.label}`;
  }).join('\n');
}

function renderAgents(agents) {
  if (!agents.length) return '_none detected_';
  return agents.map((a) => `- **${a.name}** — ${a.description.split(' ').slice(0, 14).join(' ')}…`).join('\n');
}

async function main() {
  const systemMap = await buildSystemMap(true);

  const claudeMd = readSafe(join(ROOT, 'CLAUDE.md'));
  const handoffMd = readSafe(join(ROOT, 'data', 'sessions', 'handoff.md'));

  const architecture = extractClaudeMdSections(claudeMd) || '_CLAUDE.md not found_';
  const recentWork = extractRecentHandoffs(handoffMd, 3) || '_no session history_';

  const now = new Date().toISOString();
  const sha = gitSha();

  const body = [
    '# Agent Primer — YDS Command Centre',
    '',
    `> **Generated:** ${now} · **Commit:** \`${sha}\` · **Auto-generated by \`scripts/generate-agent-primer.mjs\`** — do not edit by hand.`,
    '',
    'This file is the single context document sub-agents read before doing work. It fuses the stable architecture (from `CLAUDE.md`), the live app inventory (from the System Map service), and the last three session handoffs. If anything in here is wrong, regenerate with `npm run agent-primer`.',
    '',
    '---',
    '',
    '## 1. Architecture (from CLAUDE.md)',
    '',
    architecture,
    '',
    '---',
    '',
    '## 2. Live App Inventory',
    '',
    '### 2.1 API Routes',
    '',
    renderRoutes(systemMap.routes),
    '',
    '### 2.2 Frontend Modules (`src/js/modules/`)',
    '',
    renderModules(systemMap.modules),
    '',
    '### 2.3 Notion Databases',
    '',
    renderNotionDbs(systemMap.notionDatabases),
    '',
    '### 2.4 Google Sheets (presence only — IDs never exposed)',
    '',
    renderSheets(systemMap.sheets),
    '',
    '### 2.5 Agents',
    '',
    renderAgents(systemMap.agents),
    '',
    '---',
    '',
    '## 3. Recent Work (last 3 session handoffs)',
    '',
    recentWork,
    '',
    '---',
    '',
    '_End of primer. For full details: `.claude/docs/app-reference.md`, `.claude/docs/notion-hub.md`, `.claude/docs/tech-brief.md`._',
    '',
  ].join('\n');

  writeFileSync(PRIMER_PATH, body, 'utf8');

  const lineCount = body.split('\n').length;
  const sizeKb = (Buffer.byteLength(body, 'utf8') / 1024).toFixed(1);
  console.log(`AGENT_PRIMER.md written: ${lineCount} lines, ${sizeKb} KB`);
}

main().catch((err) => {
  console.error('generate-agent-primer failed:', err);
  process.exit(1);
});
